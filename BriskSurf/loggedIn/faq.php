    <div class="infoTitle">What are filters?</div>
    
    Filters allow you to target your website towards certain demographics. For example, if you've set filters to show your website only to people ages 25 or above, then only members who are ages 25 or above will be shown your website while they surf.
    
    
    <div class="infoTitle">Can I see my own website while surfing?</div>
    
    No. We do our best not to waste your credits. Since your credits are spent every time someone views your website, we try not to waste them on yourself.
    
    
    <div class="infoTitle">What are the benefits of becoming a premium member?</div>
    
    Well, among other things, you get more credits for the same amount of surfing. Your website is also shown to others for a larger amount of time. 
    If you refer other members, you also receive credits for websites they've viewed, and you also get 40% of their subscription fees if they become premium members.
    To view this information in more detail, please visit this page <a href="http://www.brisksurf.com/upgrade">here</a>.
    
    <div class="infoTitle">How do I earn credits?</div>
    
    The more you surf other members' websites, the more credits you earn. Becoming a premium member offers you a larger amount of credits for the time you surf.
    In addition, you also earn credits for your referred members' surfing. 
    You can also get credits from contests and promotions which we hold every now and then.

