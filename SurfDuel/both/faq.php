<div class="infoTitle">What is the voting system?</div>

Here is how it works:
<ul>
<li>If someone submits a site, then that site is shown randomly, side-by-side with another site.</li>
<li>The site that the user picks to be better gets a vote.</li>
<li>Over the course of the day, the highest voted sites are shown more and more often.</li>
<li>At midnight, the vote counter resets, and all sites are once again shown equally.</li>
<li>The top voted sites of the previous day are shown full screen to all surfers and are hailed as winners.</li>
</ul>
This way, it does not matter how many referrals you have or how much money you spent. Your site is advertised based solely on how good it is. All you have to do is keep it active.

<div class="infoTitle">What does it mean to keep my site active?</div>

Keeping a website active means to have it be shown while users surf. It’s very important to keep your sites active, otherwise they will not be shown at all.
Think of it this way: an active website means that it will be advertised, while an inactive one is useless because it’s never shown.

As a free or premium member, to keep your website active, you have to view a certain number of websites per day. If you don’t, your website will not be active the next day. As a platinum member, you don’t have to do anything to keep your site active!


<div class="infoTitle">Can I see my own website while surfing?</div>

Yes! In SurfDuel, you do not have to deal with credit nonsense, so your site is shown more the better it is. There is no harm in you seeing your own site.


